    private void BTNSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNSalvarActionPerformed
        
        
        String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        String telefone = txtTelefone.getText();
        String email = txtEmail.getText();
        String endereco = txtEndereco.getText();
        String dados = (nome+","+cpf+","+telefone+","+email+","+endereco);
        
        if(!valid.isValidCampo(nome,cpf,telefone,email,endereco)){           
                JOptionPane.showMessageDialog(null,"Campo invalido","Erro",JOptionPane.ERROR_MESSAGE );            
        }
        
        String retorno = clienteDAO.cadastrarCliente(nome,cpf,telefone,email,endereco);
        
        if(retorno == "cadastrado"){
            model.addRow(new Object[]{nome,cpf,telefone,email,endereco});
            JOptionPane.showMessageDialog(null,"Cliente Cadastrado!","Cadastro",JOptionPane.INFORMATION_MESSAGE );
            txtNome.setText("");
            txtCPF.setText("");
            txtTelefone.setText("");
            txtEmail.setText("");
            txtEndereco.setText("");
            
        }else if(retorno == "existente"){
                JOptionPane.showMessageDialog(null,"Cliente já está cadastrado","Cadastro",JOptionPane.INFORMATION_MESSAGE );

        }else{
                JOptionPane.showMessageDialog(null,"CPF ou Email invalido!","Cadastro",JOptionPane.INFORMATION_MESSAGE );

        }
        
        
        
        
    }//GEN-LAST:event_BTNSalvarActionPerformed

    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
       int valorTabela = tabela.getSelectedRow();
       String stringCPF = (String)tabela.getValueAt(valorTabela, 1);
       
       Cliente cliente = this.clienteDAO.buscarCliente(stringCPF);
       
       txtNome.setText(cliente.getNome());
       txtCPF.setText(""+cliente.getCpf());
       txtTelefone.setText(cliente.getTelefone());
       txtEmail.setText(cliente.getEmail());
       txtEndereco.setText(cliente.getEndereco());
    }//GEN-LAST:event_tabelaMouseClicked

