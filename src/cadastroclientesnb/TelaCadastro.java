    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        int valorTabela = tabela.getSelectedRow();//seleciona linha da tabela 
        if(valorTabela >=0){// verifica se alguma linha foi selecionada se não retorna que não foi selecionado linhas. 
           int result = JOptionPane.showConfirmDialog(this, "Deseja Excluir cadastro", "Excluir",
                       JOptionPane.YES_NO_OPTION, //Botoes com variaveis prédefinidas 
                        JOptionPane.QUESTION_MESSAGE);
           
           if(result == JOptionPane.YES_NO_OPTION){
                String stringCPF = (String)tabela.getValueAt(valorTabela, 1); // pega valor da posição 1 da tabela 
                JOptionPane.showMessageDialog(null,this.clienteDAO.excluirCliente(stringCPF),"Cadastro",JOptionPane.INFORMATION_MESSAGE );                
                model.removeRow(valorTabela);
           }
        }else{
               JOptionPane.showMessageDialog(null,"Nenhum cadastro selecionado","Cadastro",JOptionPane.INFORMATION_MESSAGE ); 
           }
        
        
        
    }//GEN-LAST:event_excluirActionPerformed

    private void atualizarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarCadastroActionPerformed
        int valorTabela = tabela.getSelectedRow();
        
        
        

        
        if(valorTabela >=0){
            String stringCPF = (String)tabela.getValueAt(valorTabela, 1);
            String inputBoxCPF = txtCPF.getText();
                
            if(Objects.equals(stringCPF, inputBoxCPF)){
                
                int result = JOptionPane.showConfirmDialog(this, "Deseja Atualizar este cadastro ?", "Atualizar",
                             JOptionPane.YES_NO_OPTION, //Botoes com variaveis prédefinidas 
                             JOptionPane.QUESTION_MESSAGE);

                if(result == JOptionPane.YES_NO_OPTION  ){


                    String nome = txtNome.getText();
                    String cpf = txtCPF.getText();
                    String telefone = txtTelefone.getText();
                    String email = txtEmail.getText();
                    String endereco = txtEndereco.getText();               


                    if(!valid.isValidCampo(nome,cpf,telefone,email,endereco)){           
                    JOptionPane.showMessageDialog(null,"Campo invalido","Erro",JOptionPane.ERROR_MESSAGE );            
                    }

                    String retorno = clienteDAO.alterarCliente(nome, cpf, telefone, email, endereco);

                    if(retorno == "cpfInvalid"){
                         JOptionPane.showMessageDialog(null,"CPF informado invalido","Erro",JOptionPane.ERROR_MESSAGE );
                    }else if(retorno == "emailInvalid"){
                         JOptionPane.showMessageDialog(null,"Email informado invalido","Erro",JOptionPane.ERROR_MESSAGE );
                    }else if(retorno == "notFound"){
                         JOptionPane.showMessageDialog(null,"Não foi encontrado cadastro com o CPF informado","Erro",JOptionPane.ERROR_MESSAGE );

                    }else{
                        model.removeRow(valorTabela);
                        model.addRow(new Object[]{nome,cpf,telefone,email,endereco});
                        JOptionPane.showMessageDialog(null,"Cliente atualizado com sucesso","Alteracao cadastral",JOptionPane.INFORMATION_MESSAGE );

                    }    

                }
            }else{
               JOptionPane.showMessageDialog(null,"Cadastro selecionado não possui mesmo cpf informado","Cadastro",JOptionPane.INFORMATION_MESSAGE ); 

            }
            
        }else{
               JOptionPane.showMessageDialog(null,"Nenhuma linha selecionada","Cadastro",JOptionPane.INFORMATION_MESSAGE ); 
           }
    }//GEN-LAST:event_atualizarCadastroActionPerformed

